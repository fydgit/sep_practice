package TD;

public class JAVA_LEC1 {

	public static void main(String[] args) {
		//BOOLEAN
		int a = 2;
		System.out.println(a > 4 && a < 9);
		
		int b = 5;
		System.out.println(b > 4 &&  b< 9);

		
		int x=8;
		int y=2;
		
		//addition +
		int add=x+y;
		System.out.println(add);
		
		//Subtraction -
		int sub=x-y;
		System.out.println(sub);
		
		//Multiplication *
		int mul=x*y;
		System.out.println(mul);
		
		//Division /
		int div=x/y;
		System.out.println(div);
		
		//Modulus %
		int mod=x%y;
		System.out.println(mod);
		
	
		//oparator equal to ==
		System.out.println(x == y);
		
		//oparator not equal !=
		System.out.println(x!=y);
		
		//oparator greater than >
		System.out.println(x>y);
		
		//oparator less than < 
		System.out.println(x<y);
		
		//oparator Greater than or equal to >=
		System.out.println(x>=y);
		
		//oparator less than or equal to  <=
		System.out.println(x<=y);
		
		System.out.println(x>9 && x<10);
		
		System.out.println(x>9 || x<4);
	
		  
	}
		
	}


